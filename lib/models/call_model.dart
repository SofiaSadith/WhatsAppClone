class CallModel {
  String avatar;
  String name;
  String message;
  String time;
  String date;
  bool isCalling;
  int countMessage;
  bool isLink;

  CallModel({
    required this.avatar,
    required this.name,
    required this.message,
    required this.time,
    required this.date,
    required this.isCalling,
    required this.countMessage,
    required this.isLink,
  });
}
